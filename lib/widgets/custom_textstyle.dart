import 'package:flutter/material.dart';

class CustomTextStyle {
  TextStyle style1() {
    return const TextStyle(
      fontSize: 22,
      fontWeight: FontWeight.w600,
    );
  }

  TextStyle style2() {
    return const TextStyle(
      fontSize: 18,
      fontWeight: FontWeight.w400,
      color: Colors.white,
    );
  }

  TextStyle style3() {
    return const TextStyle(
      fontSize: 16,
      fontWeight: FontWeight.w600,
      color: Colors.black54,
    );
  }
  TextStyle style4() {
    return const TextStyle(
      fontSize: 14,
      fontWeight: FontWeight.w500,
      color: Colors.white,
    );
  }
  TextStyle style5() {
    return const TextStyle(
      fontSize: 12,
      fontWeight: FontWeight.w600,
      color: Colors.black54,
    );
  }
}
